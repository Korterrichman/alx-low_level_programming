/**
 * mul - function that multiplies two integers
 * @a: formal parameter that receives the value of the first argument
 * @b: formal parameter that receives the value of the second argument
 * Return: the result of the multiplication
 */

int mul(int a, int b)
{
	long int c;

	c = a * b;
	return (c);
}
